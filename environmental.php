  <h3>Carbon Cure</h3>
  <div class="col2">
    <p>Concrete is the most used man-made material on earth and is second only to consumption of water.  Concrete itself has a minimal impact on the environment.  However, the main ingredient used to make concrete is called cement which emits Co2 during its production process.  Roughly every ton of cement that is produced releases .90 tons of carbon into the atmosphere.  The cement industry is undergoing a major transition in cement production reducing their emissions by almost 10%.   Coinciding with this change is the introduction of a cutting-edge technology called Carbon Cure.</p>

    <p>Cabon Cure technology injects captured Co2 into concrete during the mixing process.  A chemical reaction occurs converting the gas back into particle size grains of limestone.  This chemical reaction provides additional strength to our concrete mixes allowing us to use less cement in the manufacturing process and still maintain our strength requirements.  When the chemical reaction occurs it permanently locks captured carbon dioxide in concrete.  Even if the concrete is demolished, mineralized Co2 will never leak or return to the atmosphere.</p>
